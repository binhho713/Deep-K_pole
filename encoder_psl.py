def _resnet_predictor(arch, block, layers, pretrained, progress, emb_dim=512, **kwargs):
    model = ResNet_predictor(block, layers, emb_dim, **kwargs)
    return model

def _resnet_seprator(arch, block, layers, pretrained, progress, **kwargs):
    model = ResNet_seprator(block, layers, **kwargs)
    return model

def resnet18(predictor=True, emb_dim=512, pretrained=False, progress=True, **kwargs):
    """Constructs a ResNet-18 model.

    Args:
        pretrained (bool): If True, returns a model pre-trained on ImageNet
        progress (bool): If True, displays a progress bar of the download to stderr
    """
    if predictor:
        return _resnet_predictor('resnet18', BasicBlock, [2, 2, 2, 2], pretrained, progress, emb_dim=emb_dim,
                        **kwargs)
    else:
        return _resnet_seprator('resnet18', BasicBlock, [2, 2, 2, 2], pretrained, progress,
                        **kwargs)
